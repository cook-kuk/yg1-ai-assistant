import fs from "node:fs"
import path from "node:path"

const repoRoot = process.cwd()
const sourcePath = path.join(repoRoot, "data", "assets", "material_mapping_lv1_lv2_lv3.csv")
const outputPath = path.join(repoRoot, "lib", "recommendation", "shared", "material-mapping.generated.ts")

function parseCsv(content) {
  const rows = []
  let currentRow = []
  let currentCell = ""
  let inQuotes = false

  for (let index = 0; index < content.length; index += 1) {
    const char = content[index]
    const next = content[index + 1]

    if (inQuotes) {
      if (char === "\"") {
        if (next === "\"") {
          currentCell += "\""
          index += 1
        } else {
          inQuotes = false
        }
      } else {
        currentCell += char
      }
      continue
    }

    if (char === "\"") {
      inQuotes = true
      continue
    }

    if (char === ",") {
      currentRow.push(currentCell)
      currentCell = ""
      continue
    }

    if (char === "\r") continue

    if (char === "\n") {
      currentRow.push(currentCell)
      rows.push(currentRow)
      currentRow = []
      currentCell = ""
      continue
    }

    currentCell += char
  }

  if (currentCell.length > 0 || currentRow.length > 0) {
    currentRow.push(currentCell)
    rows.push(currentRow)
  }

  return rows
}

function parseCsvRecords(content) {
  const rows = parseCsv(content)
  if (rows.length === 0) return []

  const headers = rows[0].map(header => header.trim())
  return rows
    .slice(1)
    .filter(row => row.some(cell => cell.trim().length > 0))
    .map(row => {
      const record = {}
      for (let index = 0; index < headers.length; index += 1) {
        record[headers[index]] = row[index] ?? ""
      }
      return record
    })
}

if (!fs.existsSync(sourcePath)) {
  console.error(`material mapping source not found: ${sourcePath}`)
  process.exit(1)
}

const rows = parseCsvRecords(fs.readFileSync(sourcePath, "utf8"))
const output = [
  "// Generated by scripts/generate-material-mapping-module.mjs",
  `// Source: ${path.relative(repoRoot, sourcePath).replace(/\\\\/g, "/")}`,
  `export const MATERIAL_MAPPING_ROWS = ${JSON.stringify(rows, null, 2)}`,
  "",
].join("\n")

fs.writeFileSync(outputPath, output, "utf8")
console.log(`generated ${path.relative(repoRoot, outputPath)} from ${rows.length} rows`)
