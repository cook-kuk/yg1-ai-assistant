         SELECT raw_country_codes.edp_no,
            btrim(raw_country_codes.country_code) AS country_code
           FROM ( SELECT pm_1.edp_no,
                    country_row.country_code
                   FROM raw_catalog.prod_edp_option_milling pm_1,
                    LATERAL unnest(string_to_array(COALESCE(pm_1.country, ''::text), ','::text)) country_row(country_code)
