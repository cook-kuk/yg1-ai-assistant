        ), dedup_edp AS (
         SELECT ranked_edp._row_num,
            ranked_edp.idx,
            ranked_edp.root_category,
            ranked_edp.category_idx,
            ranked_edp.brand_idx,
            ranked_edp.brand_name,
            ranked_edp.series_idx,
            ranked_edp.series_name,
            ranked_edp.edp_no,
            ranked_edp.your_code,
            ranked_edp.file1,
            ranked_edp.file2,
            ranked_edp.file3,
            ranked_edp.dxf,
