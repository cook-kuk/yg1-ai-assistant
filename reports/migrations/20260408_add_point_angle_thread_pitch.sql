                 SELECT ph_1.edp_no,
                    country_row.country_code
                   FROM raw_catalog.prod_edp_option_holemaking ph_1,
                    LATERAL unnest(string_to_array(COALESCE(ph_1.country, ''::text), ','::text)) country_row(country_code)
                UNION ALL
                 SELECT pt_1.edp_no,
                    country_row.country_code
                   FROM raw_catalog.prod_edp_option_threading pt_1,
                    LATERAL unnest(string_to_array(COALESCE(pt_1.country, ''::text), ','::text)) country_row(country_code)
                UNION ALL
                 SELECT ptool_1.edp_no,
                    country_row.country_code
                   FROM raw_catalog.prod_edp_option_tooling ptool_1,
                    LATERAL unnest(string_to_array(COALESCE(ptool_1.country, ''::text), ','::text)) country_row(country_code)
                UNION ALL
                 SELECT pturn.edp_no,
                    country_row.country_code
